namespace SimulationStorm.Simulation.Presentation.Camera;

/// <summary>
/// Represents the camera translation direction.
/// </summary>
public enum CameraTranslationDirection
{
    /// <summary>
    /// The camera translates to the top left corner.
    /// </summary>
    TopLeft,
    /// <summary>
    /// The camera translates to the top side.
    /// </summary>
    Top,
    /// <summary>
    /// The camera translates to the top right corner.
    /// </summary>
    TopRight,
    /// <summary>
    /// The camera translates to the right side.
    /// </summary>
    Right,
    /// <summary>
    /// The camera translates to the bottom right corner.
    /// </summary>
    BottomRight,
    /// <summary>
    /// The camera translates to the bottom side.
    /// </summary>
    Bottom,
    /// <summary>
    /// The camera translates to the bottom left corner.
    /// </summary>
    BottomLeft,
    /// <summary>
    /// The camera translates to the left side.
    /// </summary>
    Left
}