namespace SimulationStorm.Graphics;

/// <summary>
/// Indicates how to draw a geometry.
/// </summary>
public enum PaintStyle
{
    /// <summary>
    /// Fill the geometry
    /// </summary>
    Fill,
    /// <summary>
    /// Stroke the geometry.
    /// </summary>
    Stroke,
    /// <summary>
    /// Fill and stroke the geometry.
    /// </summary>
    FillAndStroke
}