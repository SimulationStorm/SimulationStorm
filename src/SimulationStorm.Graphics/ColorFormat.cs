namespace SimulationStorm.Graphics;

/// <summary>
/// Represents the color format.
/// </summary>
public enum ColorFormat
{
    /// <summary>
    /// Represents a 32-bit color with the format RGBA.
    /// </summary>
    Rgba8888,
    
    /// <summary>
    /// Represents a 32-bit color with the format BGRA.
    /// </summary>
    Bgra8888
}